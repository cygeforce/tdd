package fragment.submissions;

public class TextOverlap {
	private String overlappedString;
	private String mergedString;

	public TextOverlap(){
		overlappedString = "";
		mergedString = "";
	}
	public String getOverlappedString() {
		return overlappedString;
	}

	public void setOverlappedString(String overlappedString) {
		this.overlappedString = overlappedString;
	}

	public String getMergedString() {
		return mergedString;
	}

	public void setMergedString(String mergedString) {
		this.mergedString = mergedString;
	}
}
